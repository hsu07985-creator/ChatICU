    w(f"| distinct ODR_CODE 數 | {r['q1_distinct_odr_codes']} |")
    w(f"| distinct 命中 drug_formulary.csv（含 ATC） | {r['q1_distinct_hit']} ({r['q1_distinct_hit_pct']}%) |")
    w(f"| 以列為權重計算的命中 | {r['q1_row_weighted_hit']} ({r['q1_row_weighted_pct']}%) |")
    w("")
    w("**判讀**：")
    w("")
    if r['q1_row_weighted_pct'] >= 70:
        w("- 列權重命中率 ≥ 70%，**可直接進 PR-3**。")
    elif r['q1_row_weighted_pct'] >= 40:
        w("- 列權重命中率 40-70%，建議**先擴 atc_drugs.csv**（針對 Q2 top-30）再進 PR-3。")
    else:
        w("- 列權重命中率 < 40%，**atc_drugs.csv 與本院 ODR_CODE 慣例差距大**，PR-3 效益有限。")
        w("  建議：(a) 花時間擴表，或 (b) 優先做 PR-2 RxNorm 自動補碼以彌補。")
    w("")
    w("## Q2：Top-30 未命中 ODR_CODE（依出現頻率）")
    w("")
    w("| # | ODR_CODE | 樣本名稱 | 筆數 | 病人數 | 在 exclusion? | 在 DDI alias? |")
    w("|--:|----------|---------|-----:|------:|:------------:|:------------:|")
    for i, u in enumerate(r['q2_top_unmapped'], 1):
        code = u['code']
        ex = "✓" if r.get("_ddi_exclusion_set") and code in r["_ddi_exclusion_set"] else ""
        al = "✓" if r.get("_ddi_alias_set") and code in r["_ddi_alias_set"] else ""
        w(f"| {i} | `{code}` | {u['sample_name'][:40] if u['sample_name'] else ''} | {u['cnt']} | {u['n_patients']} | {ex} | {al} |")
    w("")
    w(f"- Top-30 中屬於 DDI exclusion（非藥物）：{r['q2_top_unmapped_in_exclusion']} 筆")
    w(f"- Top-30 中已在 DDI alias 內（有藥名但無 ATC）：{r['q2_top_unmapped_in_ddi_alias']} 筆")
    w("")
    w("**擴表 backlog**：上表扣除 exclusion 後的項目，請在 `backend/app/fhir/code_maps/drug_formulary_gaps.csv` 的 `suggested_atc` 欄位填入 ATC，然後重跑 `build_formulary_csv.py` 合併。")
    w("")
    w("## Q3：Lab 標準碼命中率（HIS_LAB_MAP ∩ loinc_labs.csv）")
    w("")
    w("| 指標 | 值 |")
    w("|------|---:|")
    w(f"| HIS_LAB_MAP 總 LAB_CODE 數 | {r['q3_his_lab_codes_total']} |")
    w(f"| loinc_labs.csv 總 LAB_CODE 數 | {r['q3_loinc_csv_total']} |")
    w(f"| 交集（可直接對到 LOINC） | {r['q3_overlap']} ({r['q3_overlap_pct']}%) |")
    w("")
    w("**判讀**：")
    w("")
    if r['q3_overlap_pct'] >= 60:
        w("- 交集 ≥ 60%，PR-3 Lab 段可行；剩餘由 `his_lab_mapping.py` 的 ChatICU key 再查 `loinc_map.py` 做第二層對應。")
    elif r['q3_overlap_pct'] >= 30:
        w("- 交集 30-60%，建議**先跑現有 ChatICU key → LOINC 管道**（已有），不急於用 `loinc_labs.csv`。")
    else:
        w("- 交集 < 30%，`loinc_labs.csv` 對本院 LAB_CODE 意義不大。直接用 `loinc_map.py` 現成的 ChatICU key 對應即可。")
    w("")
    w("## Q4：現行 DDI 命中基準")
    w("")
