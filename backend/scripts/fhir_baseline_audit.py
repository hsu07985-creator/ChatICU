    w("|------|---:|")
    w(f"| `medications` 總筆數 | {r['medications_total_rows']} |")
    w(f"| 有 medications 的病人數 | {r['medications_distinct_patients']} |")
    w(f"| `drug_interactions` 中的唯一藥對數 | {r['q4_ddi_table_pairs']} |")
    w("")
    w("---")
    w("")
    w("## Q1：藥物標準碼命中率（ODR_CODE → drug_formulary.csv）")
    w("")
    w("| 指標 | 值 |")
    w("|------|---:|")
