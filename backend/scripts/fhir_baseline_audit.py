    w(f"| **`backend/app/fhir/code_maps/drug_formulary.csv`** | **{r['formulary_total']}** | **PRIMARY（院區形成品 + ABX + 手動補碼）** |")
    w(f"| `FHIR功能/藥物標準化/atc_drugs.csv`（舊 baseline 用） | {r['legacy_atc_csv_total']} | 多半已被 formulary 包含，保留做 `kidney_relevant` 參考 |")
