#!/usr/bin/env python3
"""PR-0 FHIR standardization baseline audit.

Read-only. Connects to Supabase via SYNC_ENV_PATH or backend/.env.his-sync.
Answers 6 questions and writes docs/fhir-baseline-report.md.

Usage:
    export SYNC_ENV_PATH=/absolute/path/to/backend/.env.his-sync
    python3 backend/scripts/fhir_baseline_audit.py
"""
from __future__ import annotations

import asyncio
import csv
import json
import os
import sys
from collections import defaultdict
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

BACKEND_ROOT = Path(__file__).resolve().parent.parent
REPO_ROOT = BACKEND_ROOT.parent
sys.path.insert(0, str(BACKEND_ROOT))

FHIR_KIT = REPO_ROOT / "FHIR功能"
LEGACY_ATC_CSV = FHIR_KIT / "藥物標準化" / "atc_drugs.csv"
LOINC_CSV = FHIR_KIT / "lab轉FHIR" / "loinc_labs.csv"
# Primary source: generated by backend/scripts/build_formulary_csv.py from
# the Yangming hospital formulary xlsx + antibiotic list xlsx.
FORMULARY_CSV = BACKEND_ROOT / "app" / "fhir" / "code_maps" / "drug_formulary.csv"
DDI_ALIAS_JSON = BACKEND_ROOT / "app" / "fhir" / "his_ddi_alias_map.json"
DDI_EXCLUSION_JSON = BACKEND_ROOT / "app" / "fhir" / "his_ddi_exclusion_list.json"

OUTPUT_MD = REPO_ROOT / "docs" / "fhir-baseline-report.md"


def get_database_url() -> str:
    env_path = os.environ.get("SYNC_ENV_PATH")
    env_file = Path(env_path) if env_path else BACKEND_ROOT / ".env.his-sync"
    if not env_file.exists():
        raise RuntimeError(f"Env file not found: {env_file}")
    for line in env_file.read_text().splitlines():
        line = line.strip()
        if line.startswith("DATABASE_URL="):
            url = line.split("=", 1)[1].strip().strip('"').strip("'")
            if url.startswith("postgresql://"):
                url = url.replace("postgresql://", "postgresql+asyncpg://", 1)
            return url
    raise RuntimeError("DATABASE_URL not in env file")


def load_atc_csv() -> dict[str, dict]:
    """Load the unified formulary CSV (Yangming formulary + ABX + legacy merged).

    Schema: odr_code, atc_code, ingredient, brand_name, unit,
            is_antibiotic, kidney_relevant, rxnorm_cui, source, notes
    """
    out = {}
    if not FORMULARY_CSV.exists():
        raise RuntimeError(
            f"{FORMULARY_CSV} not found. "
            "Run `python3 backend/scripts/build_formulary_csv.py` first."
        )
    with FORMULARY_CSV.open(encoding="utf-8") as f:
        for row in csv.DictReader(f):
            code = row["odr_code"].strip()
            if code and row.get("atc_code"):
                out[code] = row
    return out


def load_legacy_atc_csv() -> dict[str, dict]:
    """Load the legacy (pre-formulary) CSV for comparison only."""
    out = {}
    if not LEGACY_ATC_CSV.exists():
        return out
    with LEGACY_ATC_CSV.open(encoding="utf-8") as f:
        for row in csv.DictReader(f):
            out[row["odr_code"].strip()] = row
    return out


def load_loinc_csv() -> dict[str, dict]:
    out = {}
    with LOINC_CSV.open(encoding="utf-8") as f:
        for row in csv.DictReader(f):
            out[row["lab_code"].strip()] = row
    return out


def load_ddi_alias() -> dict[str, list[str]]:
    raw = json.loads(DDI_ALIAS_JSON.read_text(encoding="utf-8"))
    return {k: v for k, v in raw.items() if not k.startswith("_") and isinstance(v, list)}


def load_ddi_exclusion() -> set[str]:
    raw = json.loads(DDI_EXCLUSION_JSON.read_text(encoding="utf-8"))
    return {e["odr_code"] for e in raw.get("exclusions", []) if "odr_code" in e}


def load_his_lab_codes() -> set[str]:
    import importlib.util
    mod_path = BACKEND_ROOT / "app" / "fhir" / "his_lab_mapping.py"
    spec = importlib.util.spec_from_file_location("his_lab_mapping", mod_path)
    assert spec and spec.loader
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return set(mod.HIS_LAB_MAP.keys())


async def audit() -> dict[str, Any]:
    from sqlalchemy import text
    from sqlalchemy.ext.asyncio import create_async_engine

    atc_map = load_atc_csv()
    legacy_atc_map = load_legacy_atc_csv()
    loinc_map = load_loinc_csv()
    ddi_alias = load_ddi_alias()
    ddi_exclusion = load_ddi_exclusion()
    his_lab_codes = load_his_lab_codes()

    url = get_database_url()
    engine = create_async_engine(
        url,
        connect_args={
            "prepared_statement_cache_size": 0,
            "statement_cache_size": 0,
            "command_timeout": 120,
        },
    )

    results: dict[str, Any] = {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "formulary_total": len(atc_map),
        "legacy_atc_csv_total": len(legacy_atc_map),
        "loinc_csv_total": len(loinc_map),
        "ddi_alias_total": len(ddi_alias),
        "ddi_exclusion_total": len(ddi_exclusion),
        "his_lab_map_total": len(his_lab_codes),
    }

    async with engine.connect() as conn:
        r = await conn.execute(text("SELECT COUNT(*) FROM medications"))
        results["medications_total_rows"] = r.scalar()

        r = await conn.execute(text("SELECT COUNT(DISTINCT patient_id) FROM medications"))
        results["medications_distinct_patients"] = r.scalar()

        # Q1: ODR_CODE → ATC hit rate
        r = await conn.execute(text(
            "SELECT order_code, COUNT(*) AS cnt FROM medications "
            "WHERE order_code IS NOT NULL AND order_code <> '' "
            "GROUP BY order_code"
        ))
        odr_rows = [(row[0], row[1]) for row in r]
        distinct_odr = {c for c, _ in odr_rows}
        total_with_odr = sum(cnt for _, cnt in odr_rows)
        odr_hit_atc = {c for c in distinct_odr if c in atc_map}
        weighted_hit_atc = sum(cnt for c, cnt in odr_rows if c in atc_map)

        results["q1_distinct_odr_codes"] = len(distinct_odr)
        results["q1_rows_with_odr"] = total_with_odr
        results["q1_distinct_hit"] = len(odr_hit_atc)
        results["q1_distinct_hit_pct"] = round(100 * len(odr_hit_atc) / len(distinct_odr), 1) if distinct_odr else 0
        results["q1_row_weighted_hit"] = weighted_hit_atc
        results["q1_row_weighted_pct"] = round(100 * weighted_hit_atc / total_with_odr, 1) if total_with_odr else 0

        # Q2: Top-30 unmapped ODR_CODEs by frequency
        r = await conn.execute(text(
            "SELECT order_code, COUNT(*) AS cnt, "
            "       MAX(name) AS sample_name, "
            "       COUNT(DISTINCT patient_id) AS n_patients "
            "FROM medications "
            "WHERE order_code IS NOT NULL AND order_code <> '' "
            "GROUP BY order_code "
            "ORDER BY cnt DESC"
        ))
        all_codes = [
            {"code": row[0], "cnt": row[1], "sample_name": row[2], "n_patients": row[3]}
            for row in r
        ]
        unmapped_top = [c for c in all_codes if c["code"] not in atc_map][:30]
        results["q2_top_unmapped"] = unmapped_top

        # How many of Q2 top-30 are known non-drugs (exclusion list)?
        unmapped_top_in_exclusion = [c for c in unmapped_top if c["code"] in ddi_exclusion]
        results["q2_top_unmapped_in_exclusion"] = len(unmapped_top_in_exclusion)

        # How many of unmapped top-30 have a DDI alias already?
        unmapped_top_in_alias = [c for c in unmapped_top if c["code"] in ddi_alias]
        results["q2_top_unmapped_in_ddi_alias"] = len(unmapped_top_in_alias)

        # Q3: HIS LAB_CODE → LOINC coverage (file-to-file)
        loinc_covered = his_lab_codes & set(loinc_map.keys())
        results["q3_his_lab_codes_total"] = len(his_lab_codes)
        results["q3_loinc_csv_total"] = len(loinc_map)
        results["q3_overlap"] = len(loinc_covered)
        results["q3_overlap_pct"] = round(100 * len(loinc_covered) / len(his_lab_codes), 1) if his_lab_codes else 0

        # Q4/Q5: DDI baseline + ATC ceiling
        r = await conn.execute(text(
            "SELECT patient_id, generic_name, name, order_code "
            "FROM medications "
            "WHERE status = 'active'"
        ))
        by_patient: dict[str, list[dict]] = defaultdict(list)
        for pid, generic, name, code in r:
            by_patient[pid].append({
                "generic": generic,
                "name": name,
                "code": code,
            })

        r = await conn.execute(text("SELECT drug1, drug2 FROM drug_interactions"))
        ddi_pairs_lower: set[frozenset] = set()
        ddi_pairs_exact: set[frozenset] = set()
        ddi_name_set_lower: set[str] = set()
        ddi_name_set_exact: set[str] = set()
        for d1, d2 in r:
            if not d1 or not d2:
                continue
            d1 = d1.strip()
            d2 = d2.strip()
            if not d1 or not d2 or d1 == d2:
                continue
            ddi_pairs_exact.add(frozenset({d1, d2}))
            ddi_pairs_lower.add(frozenset({d1.lower(), d2.lower()}))
            ddi_name_set_exact.add(d1)
            ddi_name_set_exact.add(d2)
            ddi_name_set_lower.add(d1.lower())
            ddi_name_set_lower.add(d2.lower())
        results["q4_ddi_table_pairs"] = len(ddi_pairs_lower)
        results["q4_ddi_table_drugs"] = len(ddi_name_set_lower)

        total_pairs = 0
        total_current_hits_exact = 0  # replicates production case-sensitive query
        total_current_hits_lower = 0  # case-insensitive sanity check
        total_hits_with_alias = 0  # simulate: apply DDI alias map at query time
        total_atc_both_covered = 0
        drugs_matching_ddi_exact: set[str] = set()
        drugs_matching_ddi_lower: set[str] = set()
        # ODR_CODE coverage among active meds
        active_odr_in_alias = 0
        active_odr_not_in_alias = 0
        active_odr_missing = 0
        active_odr_in_atc = 0
        patient_stats = []
        for pid, meds in by_patient.items():
            active_tokens_exact: list[tuple[str, str | None]] = []  # original case
            active_tokens_lower: list[tuple[str, str | None]] = []  # lowercased
            active_tokens_alias: list[tuple[str, str | None]] = []  # if DDI alias map applied
            for m in meds:
                # Track ODR_CODE coverage
                code = m["code"]
                if not code:
                    active_odr_missing += 1
                elif code in ddi_alias:
                    active_odr_in_alias += 1
                else:
                    active_odr_not_in_alias += 1
                if code and code in atc_map:
                    active_odr_in_atc += 1

                # Build lookup tokens
                raw = (m["generic"] or m["name"] or "").strip()
                for part in raw.split(" / "):
                    p_ex = part.strip()
                    if p_ex:
                        active_tokens_exact.append((p_ex, code))
                        active_tokens_lower.append((p_ex.lower(), code))
                        if p_ex in ddi_name_set_exact:
                            drugs_matching_ddi_exact.add(p_ex)
                        if p_ex.lower() in ddi_name_set_lower:
                            drugs_matching_ddi_lower.add(p_ex.lower())

                # "As if alias map applied": use alias_map[code] when code is in map,
                # else fallback to lowered generic/name
                if code and code in ddi_alias:
                    for aliased in ddi_alias[code]:
                        if aliased:
                            active_tokens_alias.append((aliased.lower(), code))
                else:
                    for part in raw.split(" / "):
                        p_lo = part.strip().lower()
                        if p_lo:
                            active_tokens_alias.append((p_lo, code))

            seen: set[frozenset] = set()
            pair_count = 0
            hits_exact = 0
            hits_lower = 0
            atc_both = 0
            for i in range(len(active_tokens_lower)):
                for j in range(i + 1, len(active_tokens_lower)):
                    a_lo, ca = active_tokens_lower[i]
                    b_lo, cb = active_tokens_lower[j]
                    a_ex = active_tokens_exact[i][0]
                    b_ex = active_tokens_exact[j][0]
                    if a_lo == b_lo:
                        continue
                    key = frozenset({a_lo, b_lo})
                    if key in seen:
                        continue
                    seen.add(key)
                    pair_count += 1
                    if frozenset({a_ex, b_ex}) in ddi_pairs_exact:
                        hits_exact += 1
                    if key in ddi_pairs_lower:
                        hits_lower += 1
                    if ca in atc_map and cb in atc_map:
                        atc_both += 1

            # Separately count hits using alias-normalised tokens
            seen_alias: set[frozenset] = set()
            hits_alias = 0
            for i in range(len(active_tokens_alias)):
                for j in range(i + 1, len(active_tokens_alias)):
                    a_lo = active_tokens_alias[i][0]
                    b_lo = active_tokens_alias[j][0]
                    if a_lo == b_lo:
                        continue
                    key = frozenset({a_lo, b_lo})
                    if key in seen_alias:
                        continue
                    seen_alias.add(key)
                    if key in ddi_pairs_lower:
                        hits_alias += 1

            total_pairs += pair_count
            total_current_hits_exact += hits_exact
            total_current_hits_lower += hits_lower
            total_hits_with_alias += hits_alias
            total_atc_both_covered += atc_both
            patient_stats.append({
                "patient_id": pid,
                "n_active_drug_tokens": len(active_tokens_lower),
                "n_unique_pairs": pair_count,
                "current_ddi_hits_exact": hits_exact,
                "current_ddi_hits_lower": hits_lower,
                "hits_with_alias_applied": hits_alias,
                "atc_ceiling_pairs": atc_both,
            })

        patient_stats.sort(key=lambda x: -x["n_unique_pairs"])
        results["q4_total_patients_with_active"] = len(by_patient)
        results["q4_total_unique_active_pairs"] = total_pairs
        results["q4_total_current_ddi_hits_exact"] = total_current_hits_exact
        results["q4_total_current_ddi_hits_lower"] = total_current_hits_lower
        results["q4_total_hits_with_alias_applied"] = total_hits_with_alias
        results["q4_active_odr_in_alias"] = active_odr_in_alias
        results["q4_active_odr_not_in_alias"] = active_odr_not_in_alias
        results["q4_active_odr_missing"] = active_odr_missing
        results["q4_active_odr_in_atc"] = active_odr_in_atc
        results["q4_active_med_total"] = active_odr_in_alias + active_odr_not_in_alias + active_odr_missing
        results["q4_mean_pairs_per_patient"] = round(total_pairs / len(by_patient), 1) if by_patient else 0
        results["q4_drugs_matching_ddi_exact"] = sorted(drugs_matching_ddi_exact)[:20]
        results["q4_drugs_matching_ddi_lower"] = sorted(drugs_matching_ddi_lower)[:20]
        results["q4_drugs_matching_ddi_exact_count"] = len(drugs_matching_ddi_exact)
        results["q4_drugs_matching_ddi_lower_count"] = len(drugs_matching_ddi_lower)
        results["q5_total_pairs_both_atc_covered"] = total_atc_both_covered
        results["q5_atc_ceiling_pct"] = round(100 * total_atc_both_covered / total_pairs, 1) if total_pairs else 0
        results["q5_current_match_pct_exact"] = round(100 * total_current_hits_exact / total_pairs, 1) if total_pairs else 0
        results["q5_current_match_pct_lower"] = round(100 * total_current_hits_lower / total_pairs, 1) if total_pairs else 0
        results["q5_alias_applied_pct"] = round(100 * total_hits_with_alias / total_pairs, 1) if total_pairs else 0
        results["patient_stats"] = patient_stats

        # Q6: ODR_CODE overlap between his_ddi_alias_map and atc_drugs.csv
        alias_codes = set(ddi_alias.keys())
        atc_codes = set(atc_map.keys())
        overlap = alias_codes & atc_codes
        results["q6_ddi_alias_codes"] = len(alias_codes)
        results["q6_atc_csv_codes"] = len(atc_codes)
        results["q6_overlap"] = len(overlap)
        results["q6_only_alias_not_atc"] = sorted(alias_codes - atc_codes)[:20]
        results["q6_only_atc_not_alias"] = sorted(atc_codes - alias_codes)[:20]

        # Supplemental: how many ODR_CODEs in DB are covered by alias map?
        db_covered_by_alias = {c for c in distinct_odr if c in alias_codes}
        results["q6_db_odr_covered_by_alias"] = len(db_covered_by_alias)
        results["q6_db_odr_covered_by_alias_pct"] = round(100 * len(db_covered_by_alias) / len(distinct_odr), 1) if distinct_odr else 0

    await engine.dispose()
    return results


